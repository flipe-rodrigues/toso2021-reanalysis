    'linewidth',1);

% plot decision boundary
x = linspace(min(d_pairs(:,1)),max(d_pairs(:,1)),1e3);
y = -beta_d1 / beta_d2 * x - beta_0 / beta_d2;
plot(tfun(x),tfun(y),'--w',...
    'linewidth',1.5);

% plot performance
scatter(...
    tfun(d_pairset(:,1)),...
    tfun(d_pairset(:,2)),...
    250,p_choice,'s','filled',...
    'markeredgecolor','none',...
    'linewidth',1.5);

% iterate through pairs
for ii = 1 : n_pairs
    if d_pairset(ii,2) > d_pairset(ii,1)
        vertical_gain = .865;
    else
        vertical_gain = 1.175;
    end
    
    % annotate performance
    text(tfun(d_pairset(ii,1) * 1.05),tfun(d_pairset(ii,2) * vertical_gain),...
        sprintf('%.0f%%',p_choice(ii)*100),...
        'fontsize',axesopt.default.fontsize,...
        'horizontalalignment','center',...
        'verticalalignment','middle');
end

% save figure
if want2save
    svg_file = fullfile(panel_path,[fig.Name,'.svg']);
    print(fig,svg_file,'-dsvg','-painters');
end