        i1_xpatch = [flagged_time,fliplr(flagged_time)];
        i1_ypatch = [i1_mu(~nan_flags)-i1_sem(~nan_flags),...
            fliplr(i1_mu(~nan_flags)+i1_sem(~nan_flags))];
        patch_alpha = [i1_trials_throughtime(~nan_flags),...
            fliplr(i1_trials_throughtime(~nan_flags))].^0;
        patch_alpha = patch_alpha * .25 + .05;
        patch(sps(1),i1_xpatch,i1_ypatch,i1_clrs(ii,:),...
            'facevertexalphadata',patch_alpha',...
            'alphadatamapping','none',...
            'facealpha','interp',...
            'edgecolor','none');
        
