        t2_trial_mat = repmat(t2_trial_idcs,1,n_paddedtimebins);
        t2_spike_trials = t2_trial_mat(t2_spike_counts >= 1);
        t2_spike_times = t2_time_mat(t2_spike_counts >= 1);
        trial_sorter = [t1(t2_spike_flags),prev_choices(t2_spike_flags)];
        [~,sorted_idcs] = sortrows(trial_sorter,[1,-2]);
        [~,resorted_idcs] = sortrows(sorted_idcs);
        resorted_idcs = resorted_idcs + t2_n_trial_counter;
        t2_sorted_trials = resorted_idcs(t2_spike_trials - t2_n_trial_counter);
        plot(sps(4+n_cols),t2_spike_times,t2_sorted_trials,...
            'color','k',...
            'marker',spike_marker,...
            'markersize',2,...
            'linestyle','none');
        
        % plot raster bands
        xpatch = [0,1,1,0] .* range(xlim(sps(4))) * .1/2;
        ypatch = [.5,.5,t2_n_trials+.5,t2_n_trials+.5] + t2_n_trial_counter;
        patch(sps(4+n_cols),xpatch,ypatch,t2_clrs(tt,:),...
            'linewidth',1.5,...
            'facealpha',.75,...
            'edgecolor','none');
        
        % update trial counters
        t2_n_trial_counter = t2_n_trial_counter + t2_n_trials;
        t2_boundaries(tt) = t2_n_trials;
    end
    
    % iterate through choices
    for ii = 1 : n_choices
        choice_flags = choice == choice_set(ii);
        choice_spike_flags = ...
            valid_flags & ...
            neuron_flags & ...
            choice_flags;
        if sum(choice_spike_flags) == 0
            continue;
        end
        
        % fetch spike counts & compute spike rates
        choice_spike_counts = data.FR(choice_spike_flags,:);
        choice_spike_rates = conv2(...
            1,kernel.pdf,choice_spike_counts,'valid')' / psthbin * 1e3;
        choice_n_trials = size(choice_spike_counts,1);
        choice_n_tbins = post_s2_delay * psthbin;
        
        % T2-aligned, I2-split spike rates
        choice_alignment_onset = ...
            pre_init_padding + ...
            pre_t1_delay(choice_spike_flags) + ...
            t1(choice_spike_flags) + ...
            isi + ...
            t2(choice_spike_flags);
        choice_alignment_flags = ...
            valid_time >= choice_alignment_onset & ...
            valid_time < choice_alignment_onset + post_s2_delay;
        choice_chunk_flags = choice_alignment_flags;
        choice_spkrates = choice_spike_rates;
        choice_spkrates(~choice_alignment_flags') = nan;
        choice_spkrates = reshape(...
            choice_spkrates(choice_chunk_flags'),[choice_n_tbins,choice_n_trials])';
        
        % compute mean spike density function
        choice_mu = nanmean(choice_spkrates(:,time_flags),1);
        choice_std = nanstd(choice_spkrates(:,time_flags),0,1);
        choice_trials_throughtime = sum(~isnan(choice_spkrates),1);
        choice_sem = choice_std ./ sqrt(choice_trials_throughtime);
        
        % flag current stimulus period
        nan_flags = isnan(choice_mu);
        onset_flags = time2plot <= 0 & [time2plot(2:end),nan] > 0;
        offset_flags = diff([nan_flags,true]) == 1;
        flagged_time = time2plot(~nan_flags);
        
        % patch s.e.m.
        choice_xpatch = [flagged_time,fliplr(flagged_time)];
        choice_ypatch = [choice_mu(~nan_flags)-choice_sem(~nan_flags),...
            fliplr(choice_mu(~nan_flags)+choice_sem(~nan_flags))];
        patch_alpha = [choice_trials_throughtime(~nan_flags),...
            fliplr(choice_trials_throughtime(~nan_flags))].^0;
        patch_alpha = patch_alpha * .25 + .05;
        patch(sps(5),choice_xpatch,choice_ypatch,choice_clrs(ii,:),...
            'facevertexalphadata',patch_alpha',...
            'alphadatamapping','none',...
            'facealpha','interp',...
            'edgecolor','none');
        
        % patch average activity
        choice_xpatch = flagged_time(~nan_flags);
        choice_ypatch = choice_mu(~nan_flags);
        choice_ypatch(end) = nan;
        patch_alpha = choice_trials_throughtime(~nan_flags).^0;
        patch_alpha = patch_alpha * .95 + .05;
        patch(sps(5),choice_xpatch,choice_ypatch,0,...
            'facevertexalphadata',patch_alpha',...
            'alphadatamapping','none',...
            'edgealpha','interp',...
            'edgecolor',choice_clrs(ii,:),...
            'linewidth',1.5);
        
        % plot onset & offset
        plot(sps(5),time2plot(onset_flags),choice_mu(onset_flags),...
            'linewidth',1.5,...
            'marker','o',...
            'markersize',7.5,...
            'markerfacecolor','w',...
            'markeredgecolor',choice_clrs(ii,:));
        
        % plot I2 raster
        choice_time_mat = padded_time - (...
            pre_init_padding + ...
            pre_t1_delay(choice_spike_flags) + ...
            t1(choice_spike_flags) + ...
            isi + ...
            t2(choice_spike_flags));
        choice_trial_idcs = (1 : choice_n_trials)' + choice_n_trial_counter;
        choice_trial_mat = repmat(choice_trial_idcs,1,n_paddedtimebins);
        choice_spike_trials = choice_trial_mat(choice_spike_counts >= 1);
        choice_spike_times = choice_time_mat(choice_spike_counts >= 1);
        trial_sorter = [t2(choice_spike_flags),choice(choice_spike_flags)];
        [~,sorted_idcs] = sortrows(trial_sorter,[1,-2]);
        [~,resorted_idcs] = sortrows(sorted_idcs);
        resorted_idcs = resorted_idcs + choice_n_trial_counter;
        choice_sorted_trials = resorted_idcs(choice_spike_trials - choice_n_trial_counter);
        plot(sps(5+n_cols),choice_spike_times,choice_sorted_trials,...
            'color','k',...
            'marker',spike_marker,...
            'markersize',2,...
            'linestyle','none');
        
        % plot raster bands
        xpatch = [0,1,1,0] .* range(xlim(sps(5))) * .1/2;
        ypatch = [.5,.5,choice_n_trials+.5,choice_n_trials+.5] + choice_n_trial_counter;
        patch(sps(5+n_cols),xpatch,ypatch,choice_clrs(ii,:),...
            'linewidth',1.5,...
            'facealpha',.75,...
            'edgecolor','none');
        
        % update trial counters
        choice_n_trial_counter = choice_n_trial_counter + choice_n_trials;
        choice_boundaries(ii) = choice_n_trials;
    end
    
    % check if all conditions have trials
    if all(isnan(i1_boundaries)) || ...
            all(isnan(t2_boundaries)) || ...
            all(isnan(i2_boundaries)) || ...
            all(isnan(t2_boundaries)) || ...
            all(isnan(choice_boundaries))
        continue;
    end
    
    % axes updates
    set(sps(1+n_cols),...
        'ylim',[1,nansum(i1_boundaries)]+[-1,1]*.5,...
        'ytick',unique([1;cumsum(i1_boundaries,'omitnan')]));
    set(sps(2+n_cols),...
        'ylim',[1,nansum(t1_boundaries)]+[-1,1]*.5,...
        'ytick',unique(1+nansum(t1_boundaries)-[1;cumsum(t1_boundaries,'omitnan')]));
    set(sps(3+n_cols),...
        'ylim',[1,nansum(i2_boundaries)]+[-1,1]*.5,...
        'ytick',unique([1;cumsum(i2_boundaries,'omitnan')]));
    set(sps(4+n_cols),...
        'ylim',[1,nansum(t2_boundaries)]+[-1,1]*.5,...
        'ytick',unique(1+nansum(t2_boundaries)-[1;cumsum(t2_boundaries,'omitnan')]));
    set(sps(5+n_cols),...
        'ylim',[1,nansum(choice_boundaries)]+[-1,1]*.5,...
        'ytick',unique([1;cumsum(choice_boundaries,'omitnan')]));
    
    % axes linkage
    linkaxes(sps(1:n_cols),'y');
    
    % update y-axis limits
    yylim = [min([ylim(sps(1)),ylim(sps(2)),ylim(sps(3))]),...
        max([ylim(sps(1)),ylim(sps(2)),ylim(sps(3))])];
    yylim = 5 * [floor(yylim(1)/5),floor(yylim(2)/5)];
    if neurons2plot(nn) == 393
        yylim = yylim + [0,5];
    end
    yylim = max(yylim,[0,1]);
    set(sps(1:n_cols),...
        'ylim',yylim,...
        'ytick',yylim,...
        'clipping','off');
    
    % save figure
    if want2save
        try
            % save settings
            png_file = fullfile(raster_path,[get(fig,'name'),'.png']);
            print(fig,png_file,'-dpng','-r300','-painters');
            svg_file = fullfile(panel_path,[fig.Name,'.svg']);
            print(fig,svg_file,'-dsvg','-painters');
            close(fig);
        catch
            close(fig);
        end
    else
        pause(1);
        close(fig);
    end
end
