        i1_mu = nanmean(i1_spkrates(:,time_flags),1);
        i1_std = nanstd(i1_spkrates(:,time_flags),0,1);
        i1_trials_throughtime = sum(~isnan(i1_spkrates),1);
        i1_sem = i1_std ./ sqrt(i1_trials_throughtime);
        
