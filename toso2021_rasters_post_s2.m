        [~,resorted_idcs] = sortrows(sorted_idcs);
        resorted_idcs = resorted_idcs + i1_n_trial_counter;
        i1_sorted_trials = resorted_idcs(i1_spike_trials - i1_n_trial_counter);
        plot(sps(1+n_cols),i1_spike_times,i1_sorted_trials,...
            'color','k',...
            'marker',spike_marker,...
            'markersize',2,...
            'linestyle','none');
        
